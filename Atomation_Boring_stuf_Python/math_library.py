# math libray checking

import math

print("factorial:", math.factorial(5))
print("Square:", math.sqrt(2))
print("Ceil:", math.ceil(2.1))
print("Floor:", math.floor(3.5))
print("Absolute:", abs(-1))
print(math.exp(1))
print("pow:", math.pow(10, 5))
print("pi:", math.pi)
print(math.lcm(3, 12))
print(math.gcd(9, 3))
